# Sets in Python

# Sets are unordered collection of data items. 
# They store multiple items in single variable. 
# Set item are seperated by commas and enclosed withing {}.
# Sets are unchangable, you cannot change items of a set once created.
# Sets do not contain duplicate items.

s={2,4,6,2}
print(s)

info={"rahul", 34, 4.5, False}
print(info) #print order is not fixed it may change everytime you print


rahul={}    #its an empty dictionary
print(type(rahul))

rahul=set()
